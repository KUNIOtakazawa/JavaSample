package ValiableSamples;

public class Sample2_String {
    public static void main(String[] args) {
        String value1 = "Sample";

        System.out.println("*---String型のサンプル");
        System.out.println("Sampleという文字を代入した結果の出力：" + value1);
        System.out.println("*------------------------------------------------*");
    }
    /**
     * charでは1文字しか扱えませんでしたが、String型は複数文字を代入することができます。
     * 値を代入するときは、代入したい値を""(名称：ダブルクォーテーション)で囲む必要があります。
     * 複数の文字が代入できる型という認識が持てていれば大丈夫です。
     * プログラム開発では基本的にcharよりStringを使用する機会のが多いです。
     */

    /**
     * char[](配列型のchar)とStringは同じような処理を組むことができますが、扱いやすさはString型が上です。
     * 開発ではString型を使用する方が多いです。
     */
}
